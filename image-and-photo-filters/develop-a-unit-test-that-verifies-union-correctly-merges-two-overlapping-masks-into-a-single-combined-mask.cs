using System;
using System.IO;
using Aspose.Imaging;
using Aspose.Imaging.ImageOptions;
using Aspose.Imaging.Sources;
using Aspose.Imaging.MagicWand;
using Aspose.Imaging.MagicWand.ImageMasks;

class Program
{
    static void Main(string[] args)
    {
        string inputPath = "test_input.png";
        string outputPath = "output/union_result.png";

        if (!File.Exists(inputPath))
        {
            Console.Error.WriteLine($"File not found: {inputPath}");
            return;
        }

        Directory.CreateDirectory(Path.GetDirectoryName(outputPath));

        try
        {
            using (RasterImage image = (RasterImage)Image.Load(inputPath))
            {
                var mask1 = new RectangleMask(10, 10, 100, 100);
                var mask2 = new RectangleMask(50, 50, 100, 100);
                var unionMask = mask1.Union(mask2);

                bool insideBoth = unionMask.IsOpaque(60, 60);
                bool insideMask1Only = unionMask.IsOpaque(20, 20);
                bool insideMask2Only = unionMask.IsOpaque(120, 120);
                bool outsideBoth = unionMask.IsOpaque(200, 200);

                if (insideBoth && insideMask1Only && insideMask2Only && !outsideBoth)
                {
                    Console.WriteLine("Union mask verification passed.");
                }
                else
                {
                    Console.WriteLine("Union mask verification failed.");
                }

                unionMask.ApplyTo(image);
                var saveOptions = new PngOptions { Source = new FileCreateSource(outputPath, false) };
                image.Save(outputPath, saveOptions);
            }
        }
        catch (Exception ex)
        {
            Console.Error.WriteLine($"Error: {ex.Message}");
        }
    }
}

