using System;
using System.IO;
using Aspose.Imaging;
using Aspose.Imaging.FileFormats.Emf;
using Aspose.Imaging;

class Program
{
    static void Main()
    {
        try
        {
            // Hardcoded directory containing EMF files
            string inputDir = @"C:\EmfImages";

            // Retrieve all .emf files in the directory
            string[] files = Directory.GetFiles(inputDir, "*.emf");

            foreach (string inputPath in files)
            {
                // Verify the input file exists
                if (!File.Exists(inputPath))
                {
                    Console.Error.WriteLine($"File not found: {inputPath}");
                    return;
                }

                // Load the image using Aspose.Imaging
                using (Image image = Image.Load(inputPath))
                {
                    // Cast to EmfImage for cropping
                    EmfImage emf = image as EmfImage;
                    if (emf == null)
                    {
                        Console.Error.WriteLine($"Not an EMF image: {inputPath}");
                        continue;
                    }

                    // Define integer crop bounds (example: 10‑pixel inset on each side)
                    int left = 10;
                    int top = 10;
                    int width = emf.Width - 20;
                    int height = emf.Height - 20;

                    // Ensure the crop rectangle is valid
                    if (width <= 0 || height <= 0)
                    {
                        Console.Error.WriteLine($"Image too small to crop: {inputPath}");
                        continue;
                    }

                    // Perform the crop
                    var rect = new Rectangle(left, top, width, height);
                    emf.Crop(rect);

                    // Overwrite the original file
                    string outputPath = inputPath;
                    Directory.CreateDirectory(Path.GetDirectoryName(outputPath));
                    emf.Save(outputPath);
                }
            }
        }
        catch (Exception ex)
        {
            Console.Error.WriteLine($"Error: {ex.Message}");
        }
    }
}

