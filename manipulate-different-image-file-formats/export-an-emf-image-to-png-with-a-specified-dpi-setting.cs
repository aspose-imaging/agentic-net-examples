 * 2. When an application must batch‑process legacy EMF icons and export them as PNG thumbnails with a specific DPI to ensure consistent sizing across different devices.
 * 3. When a document‑generation service has to embed a vector‑based chart (EMF) into a PDF and first rasterizes it to a PNG at a known DPI for accurate printing.
 * 4. When a GIS system stores map overlays in EMF format and requires conversion to PNG with a defined DPI for use in mobile mapping apps.
