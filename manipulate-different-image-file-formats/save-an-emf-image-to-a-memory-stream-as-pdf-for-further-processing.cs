using System;
using System.IO;
using Aspose.Imaging;
using Aspose.Imaging.ImageOptions;

class Program
{
    static void Main()
    {
        // Hardcoded input and output paths
        string inputPath = @"C:\Images\sample.emf";
        string outputPath = @"C:\Images\output.pdf";

        try
        {
            // Verify input file exists
            if (!File.Exists(inputPath))
            {
                Console.Error.WriteLine($"File not found: {inputPath}");
                return;
            }

            // Ensure output directory exists
            Directory.CreateDirectory(Path.GetDirectoryName(outputPath));

            // Load the EMF image
            using (Image image = Image.Load(inputPath))
            {
                // Save to a memory stream as PDF
                using (MemoryStream ms = new MemoryStream())
                {
                    var pdfOptions = new PdfOptions();
                    image.Save(ms, pdfOptions);

                    // Example of further processing: write the PDF bytes to a file
                    File.WriteAllBytes(outputPath, ms.ToArray());
                }
            }
        }
        catch (Exception ex)
        {
            Console.Error.WriteLine($"Error: {ex.Message}");
        }
    }
}

/*
 * Real-World Use Cases:
 * 1. When a developer needs to convert a Windows Metafile (EMF) diagram generated by a reporting tool into a PDF document for email attachment without writing intermediate files.
 * 2. When an application must embed a vector‑based EMF logo into a PDF invoice that will be stored in a database as a byte array.
 * 3. When a batch job processes a folder of EMF icons and streams each one as PDF to a cloud storage API that expects a memory stream.
 * 4. When a web service receives an EMF chart from a client, converts it to PDF in memory, and returns the PDF bytes as an HTTP response.
 * 5. When a desktop utility validates the existence of an EMF file, converts it to PDF for archival, and writes the result to a predefined output directory.
